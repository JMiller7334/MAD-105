fun main(){
    var num = 5
    var add = num + 7
    var sub = num - 3
    var mult = num * 12
    var div = num/2
    var remain = num % 2

    println("$num + 7 = $add")
    println("$num - 3 = $sub")
    println("$num * 12 = $mult")
    println("$num / 2 = $div")
    println("$num % 2 = $remain")
    
}